public class DataTimbangan
{
	private Integer id;
	private String nama;
	private String jenis;
	private String url;
	private Integer nourut;
	private String ip;
	private String port;
	private String bisnisunitkode;

	public Integer getId()
	{
		return id;
	}

	public void setId(Integer id)
	{
		this.id=id;
	}

	public String getNama()
	{
		return nama;
	}

	public void setNama(String nama)
	{
		this.nama=nama;
	}

	public String getJenis()
	{
		return jenis;
	}

	public void setJenis(String jenis)
	{
		this.jenis=jenis;
	}

	public String getUrl()
	{
		return url;
	}

	public void setUrl(String url)
	{
		this.url=url;
	}

	public Integer getNourut()
	{
		return nourut;
	}

	public void setNourut(Integer nourut)
	{
		this.nourut=nourut;
	}

	public String getIp()
	{
		return ip;
	}

	public void setIp(String ip)
	{
		this.ip=ip;
	}

	public String getPort()
	{
		return port;
	}

	public void setPort(String port)
	{
		this.port=port;
	}

	public String getBisnisunitkode()
	{
		return bisnisunitkode;
	}

	public void setBisnisunitkode(String bisnisunitkode)
	{
		this.bisnisunitkode=bisnisunitkode;
	}
}
